namespace While
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // While Loop

            int i = 0;

            while (i< 5)
            {
                // Dichiarazione

                Console.WriteLine(i);
                i++; // incrementa il valore di i di 1
            }
            Console.WriteLine("Il ciclo While è terminato!");       
        }
    }
}
